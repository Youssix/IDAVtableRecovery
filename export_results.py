"""
Export vtable recovery results to JSON, C++ headers, and console output.
"""

import json
import datetime
import idaapi
import idc
import ida_name


def export_to_json(scanner, filepath):
    """
    Dump all discovered vtables, classes, and hierarchy as JSON.

    Args:
        scanner: VtableScanner instance after scan() has been called.
        filepath: Output file path.
    """
    data = {
        "meta": {
            "plugin_version": "0.3.0",
            "image_base": "0x%x" % scanner.image_base,
            "timestamp": datetime.datetime.now().isoformat(),
            "vtable_count": len(scanner.vtables),
            "rtti_count": sum(1 for v in scanner.vtables if v.is_rtti),
        },
        "vtables": [],
        "hierarchy": {},
    }

    for vt in scanner.vtables:
        entry = {
            "address": "0x%x" % vt.ea,
            "class_name": vt.class_name,
            "is_rtti": vt.is_rtti,
            "entry_count": vt.size,
            "entries": [],
            "base_classes": vt.base_classes,
        }

        for idx, fptr in enumerate(vt.func_ptrs):
            fname = ida_name.get_name(fptr) or "sub_%X" % fptr
            entry["entries"].append({
                "index": idx,
                "address": "0x%x" % fptr,
                "name": fname,
            })

        data["vtables"].append(entry)

    # Hierarchy as adjacency list
    for cls_name, bases in scanner.class_hierarchy.items():
        data["hierarchy"][cls_name] = bases

    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2)

    idaapi.msg(
        "[VtableRecovery] Exported %d vtables to %s\n"
        % (len(scanner.vtables), filepath)
    )


def export_to_header(scanner, filepath):
    """
    Generate a C++ header file with class declarations and virtual
    method signatures reconstructed from vtable analysis.

    Args:
        scanner: VtableScanner instance after scan().
        filepath: Output .h file path.
    """
    lines = []
    lines.append("#pragma once")
    lines.append("// Auto-generated by IDA Vtable Recovery v0.3.0")
    lines.append("// %s" % datetime.datetime.now().isoformat())
    lines.append("// %d vtables recovered" % len(scanner.vtables))
    lines.append("")
    lines.append("#include <cstdint>")
    lines.append("")

    # Build set of known classes for forward declarations
    all_classes = set()
    for vt in scanner.vtables:
        if vt.class_name:
            all_classes.add(vt.class_name)

    # Forward declarations
    if all_classes:
        lines.append("// Forward declarations")
        for cls in sorted(all_classes):
            sanitized = _sanitize_cpp_name(cls)
            lines.append("class %s;" % sanitized)
        lines.append("")

    # Class definitions
    for vt in scanner.vtables:
        cls_name = _sanitize_cpp_name(
            vt.class_name or "UnknownClass_%X" % vt.ea
        )

        # Determine base class specifier
        base_spec = ""
        if vt.base_classes:
            bases = ", ".join(
                "public %s" % _sanitize_cpp_name(b) for b in vt.base_classes
            )
            base_spec = " : %s" % bases

        lines.append("// vtable @ 0x%x (%d entries)" % (vt.ea, vt.size))
        if vt.is_rtti:
            raw = vt.type_desc.raw_name if vt.type_desc else "N/A"
            lines.append("// RTTI: %s" % raw)
        else:
            lines.append("// No RTTI (heuristic recovery)")
        lines.append("class %s%s {" % (cls_name, base_spec))
        lines.append("public:")

        for idx, fptr in enumerate(vt.func_ptrs):
            fname = ida_name.get_name(fptr) or ""
            # Try to guess return type and params from IDA type info
            proto = _get_func_prototype(fptr)
            if proto:
                lines.append("    virtual %s; // [%d] 0x%x" % (proto, idx, fptr))
            else:
                method_name = _clean_method_name(fname, idx)
                lines.append(
                    "    virtual void* %s(); // [%d] 0x%x"
                    % (method_name, idx, fptr)
                )

        lines.append("};")
        lines.append("")

    with open(filepath, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    idaapi.msg("[VtableRecovery] Exported C++ header to %s\n" % filepath)


def _sanitize_cpp_name(name):
    """Make a string safe for use as a C++ identifier."""
    if not name:
        return "Unknown"
    name = name.replace("::", "_")
    name = name.replace("<", "_")
    name = name.replace(">", "")
    name = name.replace(",", "_")
    name = name.replace(" ", "")
    name = name.replace("-", "_")
    # Ensure it starts with a letter or underscore
    if name and name[0].isdigit():
        name = "_" + name
    return name


def _clean_method_name(ida_name_str, index):
    """Extract a usable method name from IDA function name."""
    if not ida_name_str or ida_name_str.startswith("sub_"):
        return "vmethod_%d" % index

    # Strip common prefixes from demangled names
    # TODO: proper demangling via ida_name.demangle_name()
    name = ida_name_str
    if "?" in name:
        # MSVC mangled name, try to extract the method part
        parts = name.split("@")
        if len(parts) >= 2:
            name = parts[0].lstrip("?")

    return _sanitize_cpp_name(name) or "vmethod_%d" % index


def _get_func_prototype(ea):
    """
    Try to retrieve a function prototype string from IDA type system.
    Returns None if no type info is available.
    """
    tinfo = idaapi.tinfo_t()
    if idaapi.get_tinfo(tinfo, ea):
        proto = str(tinfo)
        if proto and proto != "?":
            return proto
    return None


def print_hierarchy_tree(scanner, output_func=None):
    """
    Print the class hierarchy as an ASCII tree to the IDA output window.

    Args:
        scanner: VtableScanner instance after scan().
        output_func: callable(str) for output. Defaults to idaapi.msg.
    """
    if output_func is None:
        output_func = idaapi.msg

    hierarchy = scanner.class_hierarchy
    if not hierarchy:
        output_func("[VtableRecovery] No class hierarchy data available.\n")
        return

    # Find root classes (classes with no bases, or whose bases are not in our set)
    known_classes = set(hierarchy.keys())
    roots = []
    for cls, bases in hierarchy.items():
        has_known_base = any(b in known_classes for b in bases)
        if not has_known_base:
            roots.append(cls)

    if not roots:
        # Fallback: just list everything
        roots = sorted(hierarchy.keys())

    # Build children map
    children = {}
    for cls, bases in hierarchy.items():
        for base in bases:
            if base in known_classes:
                children.setdefault(base, []).append(cls)

    output_func("\n=== Class Hierarchy ===\n\n")

    visited = set()

    def _print_tree(cls, prefix, is_last):
        if cls in visited:
            output_func(
                "%s%s%s (circular ref)\n"
                % (prefix, "`-- " if is_last else "|-- ", cls)
            )
            return
        visited.add(cls)

        connector = "`-- " if is_last else "|-- "
        output_func("%s%s%s" % (prefix, connector, cls))

        # Add vtable info
        vt = _find_vtable_for_class(scanner, cls)
        if vt:
            output_func("  (%d vfuncs @ 0x%x)" % (vt.size, vt.ea))
        output_func("\n")

        kids = sorted(children.get(cls, []))
        for i, child in enumerate(kids):
            child_prefix = prefix + ("    " if is_last else "|   ")
            _print_tree(child, child_prefix, i == len(kids) - 1)

    for i, root in enumerate(sorted(roots)):
        if i > 0:
            output_func("\n")
        output_func(root)
        vt = _find_vtable_for_class(scanner, root)
        if vt:
            output_func("  (%d vfuncs @ 0x%x)" % (vt.size, vt.ea))
        output_func("\n")

        kids = sorted(children.get(root, []))
        for j, child in enumerate(kids):
            _print_tree(child, "", j == len(kids) - 1)

    total = len(hierarchy)
    rtti_count = sum(1 for v in scanner.vtables if v.is_rtti)
    output_func(
        "\nTotal: %d classes, %d with RTTI, %d heuristic\n"
        % (total, rtti_count, total - rtti_count)
    )


def _find_vtable_for_class(scanner, class_name):
    """Find the VtableInfo object for a given class name."""
    for vt in scanner.vtables:
        if vt.class_name == class_name:
            return vt
    return None
